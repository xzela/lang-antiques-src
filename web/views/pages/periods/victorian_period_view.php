			<p>
				Gold and silver Scottish Victorian brooches, often depicting the foot of a grouse or a thistle, began to appear throughout Britain after Victoria and Albert bought Balmoral in Scotland in 1848. 
				Multi-colored agates were common accents. 
				Many were set with Cairgorm's, a variety of golden smoky quartz found only in the Cairngorm Mountains. 
				Cairngorms are no longer mined and today either citrine or smoky quartz is used as a substitute.
