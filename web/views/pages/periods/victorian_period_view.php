			<p>
				The British government did not require jewelers to use any hallmarking system during the 19th Century, so a characteristic of jewelry made during this time was a lack of a maker's mark or quality stampings. 
				Before 1854, most of the jewelry produced was 18k. 
				After 1854, 9k, 12k and 15k were made legal in order to compete with international markets. 
				This information is a great aid in circa dating.
			</p>

			<p>
				In the 1850's lady's clothing took an elegant turn. 
				Hair was worn, parted in the middle, in an elegant upsweep, which lent itself to diadems becoming popular and we saw the reappearance of earrings. 
				Bracelets remained in fashion, worn alone or in pairs. 
				The tours of Egyptian tombs, offered by Thomas Cook, led to a popular Egyptian Jewelry Revival. 
				Fortunato Castellani, was inspired by the ancient world and produced a collection of "archaeological" jewelry that fetched enormous prices throughout Europe.
			</p>

			<p>
				The most widespread gemstones used in jewelry during the Early Victorian Period were diamonds (rose-cuts and brilliants), amethyst, pink and golden topaz, turquoise, chalcedony, coral, garnet, ruby, seed pearls and cameos. 
				Cameos were fashioned out of many elements, including shell, lava, coral and micro-crystalline, layered quartz varieties. 
