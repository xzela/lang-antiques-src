			<p>
				The world of nature, inspired from styles of the Renaissance and MiddleAges, were very popula rmotifs in <?php echo anchor('products/type/victorian', 'Victorian Jewelry'); ?>. 
				Bouquets of flowers, branches, leaves, grapes and berries remained fashionable. 
				There was a symbolism associated with flowers that carried through the first half of the century. 
				Snake and serpent motifs reached their peak in the 1840's. 
				The snake used as a decorative motif symbolizes wisdom and eternity. 
				Victorian jewels were often set with gems that were attributed with magical properties and special meanings. 
				Seed-pearls denoted tears, and pink coral could protect one from evil and disease. 
				Love tokens and souvenirs from travel or events were cherished.
			</p>  

			<p>
				The most popular metals incorporated into the jewels of the era were 18k to 22k gold, tri-color gold, silver, rolled gold and electroplate. 
				Before the process of electroplating was discovered, less expensive jewelry pieces were produced using pinchbeck. 
				Electroplating produced far better results, as it covered the entire surface of an object with a film of gold making it superficially indistinguishable from the real thing. 
				Advancements were also made in the development of imitation stones. 
				Parian, an ivory imitation made from a type of porcelain, was carved and used for Victorian brooches and clasps.
			</p> 

			<p>
				Most of the Jewelry in the Early Victorian Period was hand manufactured, but the Industrial Revolution was introducing methods of manufacture that could greatly speed production. 
				In 1852 a method for cutting and stamping settings was developed. 
				This allowed entire pieces of jewelry to be made quickly and very inexpensively.
			</p>

			<p>
				In the late 1830's to early 40's, lady's clothing fashionably covered all of the body. 
				High necklines and bonnets covered the ears, therefore, necklaces and earrings were not often worn. 
				Extremely large brooches were in vogue, and worn at the neck during the day, or at the low décolletage, often combined with fresh flowers, for evening wear. 
				Adornment of the hands and wrists became increasingly important, with Victorian rings and large bracelets designed to make the hand look dainty and feminine.
