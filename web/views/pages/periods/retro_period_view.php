			<p>
				Clips and broockes in the 40's were swollen to a spectacular scale. 
				The ribbon bow was the most popular motif, often highlighted in the center with a calibre cut ruby or sapphire knot. 
				The Retro jewels imitate three dimensional folds of fabric which easily seperates them from the two dimensional <?php echo anchor('products/type/art-deco', 'Art Deco')?> clips or Victorian bows.
			</p>
			
