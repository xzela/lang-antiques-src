			<p>
				The United States entered the War in 1941 after the bombing of Pearl Harbor. 
				Curiously, examples of American Retro Jewelry produced at that time were whimsical and delightful. 
				Birds, baskets of flowers, dancers and other serene images were common motifs. 
				Ballerinas, bows, animals, shells, birds and hearts were also recurrent designs in jewels of the period, often accented with sprays of diamonds and twists and spirals of calibre-cut precious gems.
			</p>
