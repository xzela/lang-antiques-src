			<p>
				The most popular gemstones seen in Retro Jewelry are aquamarine, citrine, topaz, large cabochon-cut rubies and sapphires (including star stones), golden beryl, peridots and tourmaline. 
				Most of these gems were exceptionally large to reflect the scale of the Retro Jewel. 
				During the war years, smaller faceted sapphires and rubies (both natural and synthetic) were most often used as accents for the larger gems. 
				Diamonds were plentiful. 
				The demand for diamonds in <?php echo anchor('products/type/art-deco', 'Art Deco'); ?> jewelry left most of the Retro Jewelry designers with more than ample reserves of small to medium size calibre, round and baguette diamonds.
