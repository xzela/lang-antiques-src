			<p>
				Retro jewelry, or "Cocktail Jewelry" as it is sometimes called, originated in France with designs by Van Cleef &amp; Arpels. 
				They produced a collection of jewelry that was exhibited at the 1939 World's Fair in New York. 
				When the World War II broke out, the jewelry remained in New York. 
				It served as a great influence to American jewelry designers throughout the remainder of the war.
			</p>
