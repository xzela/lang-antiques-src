			<p>
				In the mid thirties, Van Cleef &amp; Arpels designed a flexible interlocking honeycomb bracelet with a large ornate buckle. 
				It was named the "Ludo" bracelet after its designer Louis "Ludovic" Arpels. 
				It was actually inspired by some of the early <?php echo anchor('products/type/victorian', 'Victorian'); ?> gold mesh garter bracelets. 
				This would become one of their most popular signature pieces. 
				It was copied by many other jewelry manufacturers and continued to be in great demand throughout the 40's.
			</p>   

			<p>
				Fine examples of Retro pins, bracelets and rings were produced in the United States by firms such as Oscar Heyman, Verdura, Black Starr &amp; Frost, Bailey, Banks and Biddle, Shreve &amp; Co., John Rubel and William Russer. 
				In Europe, Van Cleef &amp; Arpels, Cartier, Boucheron, Chamut, Trabert &amp; Hoeffer - Moubassin, Buccellati, Castelli and Bulgari all created jewels in the Retro style. 
				Cartier excelled in the style of "animalier". 
				Birds, dogs, cats and horses were created by Cartier's craftsmen and set with a variety of colored gems.
			</p>

