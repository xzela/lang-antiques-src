			<p>
				Retro jewelry is characterized by the bold, oversized and three dimensional use of rose, yellow and green, highly polished gold. 
				Retro jewels often feature massive, emerald-cut aquamarines, citrines and amethyst, accented with smaller rubies, sapphires and diamonds. 
				Retro bracelets, watches and necklaces reflected the glamour and enchantment that Hollywood inspired during times of crisis. 
				The movies provided a wartime escape into a world of fantasy and romance that was "larger than life".
			</p>   

			<p>
				In 1936, Hollywood was upstaged when King Edward VIII stepped down so he could marry Wallis Simpson. 
				The Duke and Duchess of Windsor (as they came to be known) were renowned for their collection of bold, unusual jewelry. 
				The most famous of the Duchess' Retro jewels were made by Cartier and Van Cleef &amp; Arpels.
			</p>

			<p>
				Wartime restrictions, rationing and uncertainty brought about many profound changes, but the production of jewelry was only marginally effected during World War II. 
				There was still much uncertainty about paper money. 
				In times of economic skepticism precious metal and gems represent portable capital. 
				Platinum was relatively unavailable during the war, but Retro Jewelry manufacturers used the gems and gold they had on hand to create their fashionable masterpieces. 
				Huge deposits of gems were discovered in Brazil in the 30's as the result of geological excavations searching for industrial minerals to fuel the war. 
				Hundreds of localities became known for Brazilian deposits of citrine, aquamarine, kunzite, topaz, chrysoberyl, tourmaline and amethyst.
			</p>
