			<p>
				Standards for gold in the Georgian era were usually 18kt and higher, giving the finished jewels a lovely satin luster.
				The goldsmiths of the days were not only artisans but also highly trained technicians and all items were completely handcrafted. 
				When a gold band had to be formed for a ring, this was done by melting an alloy and casting it into a bar.
				Young apprentices then hand-hammered the block of gold down to the desired thickness which the master goldsmith could use to do his magic. 
				An incredibly labor-intensive process.
			</p>

			<p>
				Around 1750, a new rolling mill was invented, that would revolutionize the appearance of all jewelry that came after. 
				It was able to roll uniform sheets of silver and gold, eliminating the need for time-consuming hand hammering.
			</p>

			<p>
				The Georgian era produced many different styles. 
				On the European continent it is often spoken of as the Louis styles, named after the French kings Louis XIV, Louis XV and Louis XVI. 
				Around the dawn of the 19th century we saw the Empire style of Napoleon Bonaparte. 
				In the century of artistic and political turmoil there were many forces at work and often at the same time.
			</p>

			<p>
				Although Louis XIV died in 1715, at the beginning of the Georgian period, he did leave a legacy on Georgian jewelry. 
				By revoking the Edict of Nantes in 1685 he caused a massive emigration of Huguenots, most of whom found refuge in countries as Germany, Holland and England. 
				A large percentage of the Huguenots were artisans and designers. 
				Louis XIV unwittingly gave the Protestant world some of the best craftsmen to be found anywhere in the Western hemisphere.
			</p>

			<p>
				One of the key designers that influenced jewelry and silversmithing in the early years of the Georgian period was Daniel Marot, whom, when in his twenties, found shelter in the then Republic of Holland. 
				Prince William of Orange, whom later became William III of England, soon discovered his talents. 
				Marot's designs for the Royal Court became all the rage amongst London and Amsterdam beau monde.
			</p> 

			<p>
				Most striking in his designs is the total symmetry and the use of fabulous hanging elements like culottes, lambrequins and tassels. 
				Marot's view on the Baroque influenced the world of art well until the 1750's.
			</p>
