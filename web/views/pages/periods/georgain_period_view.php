			<p>
				Navette, oval and rectangular formed rings, finely worked with royal blue glass or enamel and topped with silver inlays, were in fashion. 
				Often the inlays consisted of Roman vases which were delicately embedded with diamonds. 
				Rings, brooches and pendants with natural pearl rims also were en vogue.
			</p>

			<p>
				Memento Mori jewelry from the renaissance was reintroduced. 
				Loved ones requested locks of hair from each other to be incorporated in brooches and rings to commemorate them while far away. 
				Jewelry made of hair came to a peak around the end of the Georgian era when complete sets including bracelets and necklaces were woven following the many published designs on this craft. 
				Cameos, often joined by a series of delicate chains to form fabulous necklaces, were some of the fashionable items as well as sevignes, rivieres and chatelaines.
			</p>

			<p>
				The so-called girandoles (ribbon bows supporting pear shaped gemstones such as garnets) were in high demand during the Georgian period, along with crescent, flower-head and star-bust brooches. 
				Napoleon Bonaparte was so fond of jewelry that he founded a cameo carving school which was directed by Roman Vincent. 
				One of the finest carvers at the end of the Georgian period was Benedetto Pistrucci.
			</p>

			<p>
				The Napoleonic war against Egypt at the end of the century gave rise to a whole range of new ornamentation  - sphinxs', pyramids and papyrus leaves were incorporated into the jewelry of the day. 
				Bracelets were worn in pairs as they were for centuries.
			</p>

			<p>
				Legend has it that when Josephine de Beauharnais (Napoleon's first wife) was presented flowers during an official visit, she handed a customary bracelet to the young flower girl. 
				The fashion of wearing a bracelet on each arm was so common that the girl instinctively offered her other arm. 
				Josephine gracefully solved it by giving the girl one of her own.
			</p>

			<p>
				The White House in Washington D.C. is one of the finest examples of Neo-Classisim architecture in the USA.
			</p>

			<p>
				Around 1804 in Prussia, now Germany/Poland, the wealthy were called upon to give up their jewelry in order to finance the war against Napoleon. 
				They were thanked with iron jewelry relacements, often inscribed with "Ich gab Gold fur Eissen" (I gave gold for iron). 
				Those delicate 'Fer-de-Berlin' objects are highly collectible in present time.
			</p>

			<p>
				This was also the beginning of the first French Empire led by Napoleon. 
				Favorite motifs included non-naturalistic flowers, Greek keys, laurel leaves, eagles and arches. 
				From Italy came fabulous mosaics that were incorporated in necklaces, rings and brooches.
			</p> 
			
			<p>
				The beautiful Roman mosaics ('Mille-Fiore') were made up of very small handcrafted glass squares, called tesserae. 
				Pietra-dura mosaics from Florence were composed of hand-sawed semi-precious stones which were then inlayed in black onyx to create a dramatic effect.
			</p>

			<p>
				After the fall of Napoleon we saw the rise of finely detailed cannetille work. 
				Very thin gold wires and granules decorated the detailed repousse ornaments. 
				The long wars made diamonds so scarce that courser settings were used to make them appear larger. 
				Coral came back in fashion after a long hiatus. 
				The new Neo-Gothic style formed the prelude to eclecticism in the 19th century.
