			<p>
				In the European fashion capital of Paris, the Louis XIV style influenced by Jean Berain was also dominated by complete symmetry. 
				C-shaped volutes in the traditional repousse technique are also very characteristic.
			</p>

			<p>
				Around 1725, when Louis XV was old enough to reign France, a new creative breeze blew through Paris.
				Artists freed themselves from the heavy Baroque style of Berain and the Rococo was born. 
				Lines became fluent and asymmetrical. 
				Jewelry became more frivolous and airy following the Baroque-Regence style between 1715 and 1725. 
				The motifs seemed to want to break out of the overall form. 
				Jewelers through out Europe soon followed this style but only really adopted it in the 1740's, although England almost entirely skipped this style. 
				Shell motifs and S-shaped lines were highly popular.
			</p>

			<p>
				Circa 1760, a massive shift towards Neo-Classicism in the jewelry arts was fueled by designers such as Robert Adam.
				The excavations of Pompeii and Herculaneum some decades earlier planted seeds in romantic minds that would come to full fruition in the <?php echo anchor('products/type/victorian', 'Victorian');?> period. 
				When Goethe published his "The Sorrows of Young Werther" in 1774, young men in central Europe identified themselves with a modern day Narcissus and followed Werther's fatal example in large numbers.
			</p> 

			<p>
				Artists found inspiration in Roman history and the assumed pure values of morality in ancient times. 
				The established powers of the day probably never had the slightest thought that the new found ideas of self determination and freedom sweeping the continent and North America would soon seal their fates. 
				The Western world was about to change dramatically.
			</p>

			<p>
				Currently jewelry pieces from this Neo-Classicism period are amongst the most sought after items.
			</p>
