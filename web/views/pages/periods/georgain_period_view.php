			<p>
				Jewelry from Georgian times is relatively rare as when time passed most was taken apart and refitted to reflect the modern trends of the time. 
				Materials like gold, silver and diamonds were very scarce and expensive and little consideration for posterity was taken when the fine period pieces had to be dismantled in order to create new jewelry. 
				Something one would not easily do today. 
				The few exceptional pieces that have survived all show extraordinary craftsmanship.
			</p>

			<p>
				Most <?php echo anchor('products/type/georgian', 'Georgian Jewelry')?> available is from the later dates characterized by highly dimensional repousse. 
				Floral and scroll motifs are typical of the period and the most common stones used are garnets, precious topaz, coral and early faceted diamonds set in silver.
			</p>

			<p>
				During most of the 18th century, diamonds were set in closed silver-settings and in order to protect the skin from tarnish, the interior of the pieces was crafted in gold. 
				Jewelry was custom designed to suit the diamonds that were available. 
				Stones were never specially cut for the pieces. 
				The most common cuts used in Georgian designs were rose-cuts and table-cuts. 
				The setters routinely backed the rose-cut diamonds with a reflecting foil to enhance the beauty of the diamonds and so creating the beloved fire, which is best seen under candlelight.
			</p>

			<p>
				Care must be taken when wearing these Georgian foil backed diamonds as the foil will gradually lose its effect after it has come in contact with water. 
				The best way to clean the setting is to use a soft brush and chalk powder to act as a polisher. 
				It takes a highly skilled goldsmith to professionally replace the foil. 
				We at Lang Antiques pride ourselves as being one of the rare jewelry houses that can provide this restoration service.
			</p>
